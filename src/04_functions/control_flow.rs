//! Control Flow - р╕Бр╕▓р╕гр╕Др╕зр╕Ър╕Др╕╕р╕бр╕Бр╕▓р╕гр╕Чр╕│р╕Зр╕▓р╕Щр╕Вр╕нр╕Зр╣Вр╕Ыр╕гр╣Бр╕Бр╕гр╕б
//!
//! р╣Др╕Яр╕ер╣Мр╕Щр╕╡р╣Йр╕кр╕нр╕Щр╣Ар╕гр╕╖р╣Ир╕нр╕З if expressions, loops (loop, while, for), match expressions
//! р╣Бр╕ер╕░р╕Бр╕▓р╕гр╣Гр╕Кр╣Йр╕Зр╕▓р╕Щ control flow р╕Хр╣Ир╕▓р╕Зр╣Ж р╣Гр╕Щ Rust
//! (р╕ир╕╣р╕Щр╕вр╣Мр╕Др╕зр╕Ър╕Др╕╕р╕бр╕Бр╕▓р╕гр╕Ър╕┤р╕Щ: р╕Ьр╕╣р╣Йр╕Бр╕│р╕Бр╕▒р╕Ър╕Бр╕▓р╕гр╣Бр╕кр╕Фр╕Зр╣Вр╕Др╣Йр╕Ф! ЁЯОмЁЯОо)

/// р╕Яр╕▒р╕Зр╕Бр╣Мр╕Кр╕▒р╕Щр╕кр╕│р╕лр╕гр╕▒р╕Ър╕кр╕нр╕Щ Control Flow
/// р╕бр╕▓р╣Ар╕гр╕╡р╕вр╕Щр╕гр╕╣р╣Йр╕Бр╕▓р╕гр╕Др╕зр╕Ър╕Др╕╕р╕бр╕Бр╕▓р╕гр╕Чр╕│р╕Зр╕▓р╕Щр╕Вр╕нр╕Зр╣Вр╕Ыр╕гр╣Бр╕Бр╕гр╕бр╕Бр╕▒р╕Щр╣Ар╕Цр╕нр╕░! ЁЯОо
/// (р╕вр╕┤р╕Щр╕Фр╕╡р╕Хр╣Йр╕нр╕Щр╕гр╕▒р╕Ър╕кр╕╣р╣Ир╕ир╕╣р╕Щр╕вр╣Мр╕Др╕зр╕Ър╕Др╕╕р╕бр╕Бр╕▓р╕гр╕Ър╕┤р╕Щ! ЁЯЫйя╕П)
pub fn learn_control_flow() {
    println!("\nЁЯОо === Control Flow: р╕ир╕╣р╕Щр╕вр╣Мр╕Др╕зр╕Ър╕Др╕╕р╕бр╕Бр╕▓р╕гр╕Ър╕┤р╕Щ! === ЁЯОо");

    // if expressions (р╕Щр╕▒р╕Бр╕Хр╕▒р╕Фр╕кр╕┤р╕Щр╣Гр╕И! тЪЦя╕П)
    let number = 6;  // р╕Хр╕▒р╕зр╣Ар╕ер╕Вр╕Ьр╕╣р╣Йр╕Цр╕╣р╕Бр╕Юр╕┤р╕Юр╕▓р╕Бр╕йр╕▓! ЁЯОп
    if number % 4 == 0 {  // р╣Ар╕Зр╕╖р╣Ир╕нр╕Щр╣Др╕Вр╣Бр╕гр╕Б! 1я╕ПтГг
        println!("ЁЯФв {number} р╕лр╕▓р╕гр╕Фр╣Йр╕зр╕в 4 р╕ер╕Зр╕Хр╕▒р╕з (р╣Ар╕Ыр╣Кр╕░р╕бр╕▓р╕Б! ЁЯОп)");
    } else if number % 3 == 0 {  // р╣Ар╕Зр╕╖р╣Ир╕нр╕Щр╣Др╕Вр╕Чр╕╡р╣Ир╕кр╕нр╕З! 2я╕ПтГг
        println!("ЁЯФв {number} р╕лр╕▓р╕гр╕Фр╣Йр╕зр╕в 3 р╕ер╕Зр╕Хр╕▒р╕з (р╣Гр╕Бр╕ер╣Йр╣Бр╕ер╣Йр╕з! ЁЯОк)");
    } else if number % 2 == 0 {  // р╣Ар╕Зр╕╖р╣Ир╕нр╕Щр╣Др╕Вр╕Чр╕╡р╣Ир╕кр╕▓р╕б! 3я╕ПтГг
        println!("ЁЯФв {number} р╣Ар╕Ыр╣Зр╕Щр╣Ар╕ер╕Вр╕Др╕╣р╣И (р╕Др╕╣р╣Ир╣Гр╕И! ЁЯТС)");
    } else {  // р╕Бр╕гр╕Ур╕╡р╕нр╕╖р╣Ир╕Щр╣Ж! ЁЯд╖тАНтЩВя╕П
        println!("ЁЯФв {number} р╣Ар╕Ыр╣Зр╕Щр╣Ар╕ер╕Вр╕Др╕╡р╣И (р╣Ар╕Фр╕╡р╣Ир╕вр╕зр╕Фр╕▓р╕в! ЁЯХ║)");
    }

    // if р╣Гр╕Щ let statement (р╕Бр╕▓р╕гр╕Хр╕▒р╕Фр╕кр╕┤р╕Щр╣Гр╕Ир╣Бр╕Ър╕Ър╣Ар╕гр╣Зр╕з! тЪб)
    let condition = true;  // р╣Ар╕Зр╕╖р╣Ир╕нр╕Щр╣Др╕Вр╕Др╕зр╕▓р╕бр╕Ир╕гр╕┤р╕З! тЬЕ
    let number = if condition { 5 } else { 6 };  // р╣Ар╕ер╕╖р╕нр╕Бр╣Бр╕Ър╕Ър╕Йр╕▒р╕Ър╣Др╕з! ЁЯПГтАНтЩВя╕П
    println!("ЁЯОп р╕Др╣Ир╕▓р╕Чр╕╡р╣Ир╣Др╕Фр╣Й: {number} (р╕Хр╕▒р╕Фр╕кр╕┤р╕Щр╣Гр╕Ир╣Бр╕ер╣Йр╕з! ЁЯОк)");

    // loop - р╕зр╕Щр╕ер╕╣р╕Ыр╣Др╕бр╣Ир╕кр╕┤р╣Йр╕Щр╕кр╕╕р╕Ф (р╣Ар╕Др╕гр╕╖р╣Ир╕нр╕Зр╕Ир╕▒р╕Бр╕гр╕Щр╕┤р╕гр╕▒р╕Щр╕Фр╕гр╣М! ЁЯФД)
    println!("\nЁЯФД === Loops: р╕кр╕зр╕Щр╕кр╕Щр╕╕р╕Бр╣Бр╕лр╣Ир╕Зр╕Бр╕▓р╕гр╕зр╕Щр╕Лр╣Йр╕│! === ЁЯФД");
    let mut counter = 0;  // р╕Хр╕▒р╕зр╕Щр╕▒р╕Ър╕Ьр╕╣р╣Йр╕Бр╕ер╣Йр╕▓р╕лр╕▓р╕Н! ЁЯзо
    let result = loop {  // р╕ер╕╣р╕Ыр╕нр╕Щр╕▒р╕Щр╕Хр╣М! тЩ╛я╕П
        counter += 1;  // р╕Щр╕▒р╕Ър╣Др╕Ыр╣Ар╕гр╕╖р╣Ир╕нр╕вр╣Ж! тмЖя╕П
        if counter == 3 {  // р╣Ар╕бр╕╖р╣Ир╕нр╕Цр╕╢р╕Зр╣Ар╕Ыр╣Йр╕▓р╕лр╕бр╕▓р╕в! ЁЯОп
            break counter * 2; // р╕лр╕ер╕╕р╕Фр╕нр╕нр╕Бр╕бр╕▓р╕Юр╕гр╣Йр╕нр╕бр╕Вр╕нр╕Зр╕гр╕▓р╕Зр╕зр╕▒р╕е! ЁЯОБ
        }
    };
    println!("ЁЯФв р╕Ьр╕ер╕ер╕▒р╕Юр╕Шр╣Мр╕Ир╕▓р╕Б loop: {result} (р╕лр╕ер╕╕р╕Фр╕нр╕нр╕Бр╕бр╕▓р╣Бр╕ер╣Йр╕з! ЁЯПГтАНтЩВя╕ПЁЯТи)");

    // while loop (р╕ер╕╣р╕Ыр╕Щр╕▒р╕Бр╕гр╕н! тП│)
    let mut number = 3;  // р╣Ар╕Др╕гр╕╖р╣Ир╕нр╕Зр╕Щр╕▒р╕Ър╕Цр╕нр╕вр╕лр╕ер╕▒р╕З! тП░
    while number != 0 {  // р╕гр╕нр╕Ир╕Щр╕Бр╕зр╣Ир╕▓р╕Ир╕░р╣Ар╕Ыр╣Зр╕Щ 0! ЁЯОп
        println!("тП░ {number} (р╕Щр╕▒р╕Ър╕Цр╕нр╕вр╕лр╕ер╕▒р╕З! ЁЯУЙ)");
        number -= 1;  // р╕ер╕Фр╕ер╕Зр╕Чр╕╡р╕ер╕░ 1! тмЗя╕П
    }
    println!("ЁЯЪА р╕Ыр╕ер╣Ир╕нр╕вр╕Ир╕гр╕зр╕Ф! (р╕Ър╕┤р╕Щр╣Бр╕ер╣Йр╕з! ЁЯММ)");

    // for loop р╕Бр╕▒р╕Ъ array (р╕Чр╕▒р╕зр╕гр╣Мр╕Кр╕бр╕кр╕зр╕Щр╕Ьр╕ер╣Др╕бр╣Й! ЁЯНУ)
    let fruits = ["р╣Бр╕нр╕Ыр╣Ар╕Ыр╕┤р╣Йр╕е", "р╕Бр╕ер╣Йр╕зр╕в", "р╕кр╣Йр╕б"];  // р╕Хр╕░р╕Бр╕гр╣Йр╕▓р╕Ьр╕ер╣Др╕бр╣Й! ЁЯз║
    for fruit in fruits {  // р╣Ар╕Фр╕┤р╕Щр╕Кр╕бр╕Чр╕╡р╕ер╕░р╕Ьр╕е! ЁЯЪ╢тАНтЩВя╕П
        println!("ЁЯНО р╕Ьр╕ер╣Др╕бр╣Й: {fruit} (р╕нр╕гр╣Ир╕нр╕вр╕Ир╕▒р╕З! ЁЯШЛ)");
    }

    // for loop р╕Бр╕▒р╕Ъ range (р╕Чр╕▒р╕зр╕гр╣Мр╕Кр╕бр╕Хр╕▒р╕зр╣Ар╕ер╕В! ЁЯФв)
    for number in 1..4 {  // р╣Ар╕Фр╕┤р╕Щр╕Чр╕▓р╕Зр╕Ир╕▓р╕Б 1 р╕Цр╕╢р╕З 3! ЁЯЪ╢тАНтЩВя╕П
        println!("ЁЯФв р╕Хр╕▒р╕зр╣Ар╕ер╕В: {number} (р╕кр╕зр╕вр╕Зр╕▓р╕б! тЬи)");
    }

    // for loop р╕Бр╕▒р╕Ъ index (р╕Чр╕▒р╕зр╕гр╣Мр╣Бр╕Ър╕Ър╕бр╕╡р╣Др╕Бр╕Фр╣М! ЁЯЧ║я╕П)
    for (index, value) in fruits.iter().enumerate() {  // р╣Ар╕Фр╕┤р╕Щр╕Кр╕бр╕Юр╕гр╣Йр╕нр╕бр╕лр╕бр╕▓р╕вр╣Ар╕ер╕В! ЁЯУЛ
        println!("ЁЯУН р╕ер╕│р╕Фр╕▒р╕Ър╕Чр╕╡р╣И {}: {} (р╕Ир╕Фр╕Ър╕▒р╕Щр╕Чр╕╢р╕Бр╣Бр╕ер╣Йр╕з! ЁЯУЭ)", index + 1, value);
    }

    // match expression (р╕ир╕▓р╕ер╕вр╕╕р╕Хр╕┤р╕Шр╕гр╕гр╕бр╣Бр╕лр╣Ир╕Зр╕Бр╕▓р╕гр╕Ир╕▒р╕Ър╕Др╕╣р╣И! тЪЦя╕П)
    println!("\nЁЯОп === Match Expression: р╕ир╕▓р╕ер╕вр╕╕р╕Хр╕┤р╕Шр╕гр╕гр╕б! === ЁЯОп");
    let dice_roll = 4;  // р╕ер╕╣р╕Бр╣Ар╕Хр╣Лр╕▓р╕Ьр╕╣р╣Йр╕Цр╕╣р╕Бр╕Юр╕┤р╕Юр╕▓р╕Бр╕йр╕▓! ЁЯО▓
    match dice_roll {  // р╣Ар╕Ыр╕┤р╕Фр╕ир╕▓р╕е! ЁЯСитАНтЪЦя╕П
        1 => println!("ЁЯО▓ р╣Др╕Фр╣Й 1 - р╣Ар╕гр╕┤р╣Ир╕бр╕Хр╣Йр╕Щр╣Гр╕лр╕бр╣И! (р╣Вр╕Кр╕Др╕гр╣Йр╕▓р╕в! ЁЯШЕ)"),
        2 | 3 => println!("ЁЯО▓ р╣Др╕Фр╣Й {dice_roll} - р╕ер╕нр╕Зр╕нр╕╡р╕Бр╕Др╕гр╕▒р╣Йр╕З! (р╣Ар╕Бр╕╖р╕нр╕Ър╣Бр╕ер╣Йр╕з! ЁЯдП)"),
        4..=6 => println!("ЁЯО▓ р╣Др╕Фр╣Й {dice_roll} - р╣Ар╕Бр╣Ир╕Зр╕бр╕▓р╕Б! (р╕Кр╕Щр╕░р╣Бр╕ер╣Йр╕з! ЁЯПЖ)"),
        _ => println!("ЁЯО▓ р╕ер╕╣р╕Бр╣Ар╕Хр╣Лр╕▓р╣Бр╕Ыр╕ер╕Бр╣Ж (р╕бр╕▓р╕Ир╕▓р╕Бр╕бр╕┤р╕Хр╕┤р╣Др╕лр╕Щ? ЁЯЫ╕)"),
    }

    // match р╕Бр╕▒р╕Ъ Option (р╕Хр╕╣р╣Йр╣Ар╕Лр╕Яр╕ер╕╢р╕Бр╕ер╕▒р╕Ъ! ЁЯФР)
    let maybe_number = Some(5);  // р╕Бр╕ер╣Ир╕нр╕Зр╕Чр╕╡р╣Ир╕нр╕▓р╕Ир╕бр╕╡р╕Вр╕нр╕З! ЁЯУж
    match maybe_number {  // р╣Ар╕Ыр╕┤р╕Фр╕Бр╕ер╣Ир╕нр╕З! ЁЯОБ
        Some(x) => println!("ЁЯОБ р╕бр╕╡р╕Др╣Ир╕▓: {x} (р╣Вр╕Кр╕Др╕Фр╕╡! ЁЯНА)"),
        None => println!("ЁЯУн р╣Др╕бр╣Ир╕бр╕╡р╕Др╣Ир╕▓ (р╕Бр╕ер╣Ир╕нр╕Зр╕зр╣Ир╕▓р╕З! ЁЯШв)"),
    }

    println!("тЬЕ р╣Ар╕гр╕╡р╕вр╕Щр╕гр╕╣р╣Й Control Flow р╣Ар╕кр╕гр╣Зр╕Ир╣Бр╕ер╣Йр╕з! (р╕Ыр╕┤р╕Фр╕ир╕╣р╕Щр╕вр╣Мр╕Др╕зр╕Ър╕Др╕╕р╕б! ЁЯОмЁЯОЙ)");
}
